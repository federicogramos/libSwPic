#include <project.h>
#include <adc12f.h>


/*******************************************************************************
 * @brief Globals
 ******************************************************************************/

int adc12f_val = 0;


/*******************************************************************************
 * @brief
 ******************************************************************************/

void adc12f_driver(void) {
	adc12f_val = 0;
	adc12f_val |= ADRESL << 0;	// LSB.
	adc12f_val |= ADRESH << 8;	// MSB.
}

